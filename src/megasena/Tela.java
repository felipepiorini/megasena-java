    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed

        if (jTFNumero.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite um número", "info", JOptionPane.INFORMATION_MESSAGE);
            jTFNumero.requestFocusInWindow();
        } else {
            int num = Integer.parseInt(jTFNumero.getText());
            if (num < 1 || num > 60) {
                JOptionPane.showMessageDialog(null, "Só é permitido apostar valores entre 1 e 60", "info", 0);
                jTFNumero.setText("");
                jTFNumero.requestFocusInWindow();
            } else {
                int retorno = matriz.add(num);
                if (retorno == -1) {
                    JOptionPane.showMessageDialog(null,
                            "Aposta máxima é de 10 números", "Aviso", 0);
                } else if (retorno == 0) {
                    JOptionPane.showMessageDialog(null,
                            "Valor já cadastrado", "Aviso", 0);
                }

                int valor = Integer.parseInt(jTFNumero.getText());
                jTFNumero.setText("");
                jTASorteio.setText("");
                jTADados.setText("");
                jTFNumero.requestFocusInWindow();
                jTRecebeN.setText(matriz.toString());
                jTCont.setText(String.valueOf(matriz.getContN()));
                jTVAposta.setText(matriz.toStringValorAposta());

                matriz.clearCont();
                setEstado();
            }
        }
    }//GEN-LAST:event_jBAddActionPerformed

