    private void jBConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConfirmaActionPerformed
        if (matriz.getContN() < 6) {
            JOptionPane.showMessageDialog(null, "Aposta mínima de 6 números", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            jTFNumero.requestFocusInWindow();
        } else if (matriz.getContN() > 10) {
            JOptionPane.showMessageDialog(null, "Aposta máxima é de 10 números", "Atenção", JOptionPane.INFORMATION_MESSAGE);
        } else {
            matriz.geraNumeros();
            jTASorteio.setText(matriz.toStringSorteio());
            jTADados.setText("");
            setEstado();
        }
    }//GEN-LAST:event_jBConfirmaActionPerformed

    private void jBResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBResultadoActionPerformed
        int acertos = matriz.qtdeElementosIguais();
        if (acertos == 0) {
            jTADados.setText("Infelizmente você não acertou \nnenhum número, tente novamente!");
        } else if (acertos == 6) {
            jTADados.setText("Você Acertou todos os números!!!");
        } else {
            jTADados.setText("Números apostados:\n" + matriz.toString()
                    + "\nVocê apostou " + matriz.getContN() + " números"
                    + "\nQtde de acertos: " + acertos);

        }


    }//GEN-LAST:event_jBResultadoActionPerformed

    private void jTFNumeroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFNumeroKeyTyped
        char tecla = evt.getKeyChar();
        if (!Character.isDigit(tecla)) {
            evt.consume();
        }
    }//GEN-LAST:event_jTFNumeroKeyTyped

